package org.java.ssm.entity;

import java.io.Serializable;

public class ReturnGoods implements Serializable{
    private Integer id;

    private Integer goodsId;

    private Integer preAmount;

    private Double xiaoji;

    private Integer recordeId;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public Integer getGoodsId() {
        return goodsId;
    }

    public void setGoodsId(Integer goodsId) {
        this.goodsId = goodsId;
    }

    public Integer getPreAmount() {
        return preAmount;
    }

    public void setPreAmount(Integer preAmount) {
        this.preAmount = preAmount;
    }

    public Double getXiaoji() {
        return xiaoji;
    }

    public void setXiaoji(Double xiaoji) {
        this.xiaoji = xiaoji;
    }

    public Integer getRecordeId() {
        return recordeId;
    }

    public void setRecordeId(Integer recordeId) {
        this.recordeId = recordeId;
    }
}